# Create a sequence of numbers from 20 to 50
sequence_20_to_50 <- seq(20, 50, by = 1)

# Calculate the mean of numbers from 20 to 60
mean_20_to_60 <- mean(seq(20, 60, by = 1))

# Calculate the sum of numbers from 51 to 91
sum_51_to_91 <- sum(seq(51, 91, by = 1))

# Display the results
cat("Sequence of numbers from 20 to 50:\n")
print(sequence_20_to_50)

cat("\nMean of numbers from 20 to 60:\n")
print(mean_20_to_60)

cat("\nSum of numbers from 51 to 91:\n")
print(sum_51_to_91)
